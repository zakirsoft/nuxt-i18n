<template>
  <label class="pure-material-textfield-outlined">
    <input v-model="localValue" :type="type" placeholder=" " :required="required">
    <span>{{ label }}</span>
  </label>
</template>

<script>
export default {
  name: 'MaterialInput',
  props: {
    label: {
      type: String,
      default: ''
    },
    value: {
      type: String,
      default: ''
    },
    type: {
      type: String,
      default: 'text'
    },
    required: {
      type: Boolean,
      default: false
    }
  },
  computed: {
    localValue: {
      get () {
        return this.value
      },
      set (val) {
        this.$emit('input', val)
      }
    }
  }
}
</script>

